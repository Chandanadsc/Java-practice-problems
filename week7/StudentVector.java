package week7;

public class StudentVector {

		private String name;
	    private int id;

	    public StudentVector(String name, int id) {
	        this.name = name;
	        this.id = id;
	    }

	    public String getName() {
	        return name;
	    }

	    public int getId() {
	        return id;
	    }
	


}
